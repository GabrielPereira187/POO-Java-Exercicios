
package fatec.poo.model;

/**
 *
 * @author gdeba
 */
public class Pessoa {
    private String nome;
    private String tel;

    public Pessoa(String nome, String tel) {
        this.nome = nome;
        this.tel = tel;
    }

    public String getNome() {
        return nome;
    }

    public String getTel() {
        return tel;
    }
    
    
}
